class Highscore
{
private:
    string name;
    int attempts;

public:
// fixa de här, de ska hantera flera namn
    optional<string> load(const string& filename) {
      ifstream inputFile(filename);
      if (inputFile) {
        inputFile >> name >> attempts;
        return nullopt;
      } else {
        return "Failed to open file for reading.";
      }
    }

    optional<string> save (const string& filename) const {
        ofstream inputFile(filename);
        if (inputFile) {
            inputFile << name << " " << attempts << endl;
            return nullopt;
        } else {
            return "Failed to open file for writing.";
        }
    }

    bool isNewHighScore (int newAttempts) const {
        return attempts > newAttempts;
    }
};

class Game
{
private:
    string wordToGuess;
    string guessedLetters;
    vector<char> incorrectGuesses;
    int maxAttempts;

public:
    Game(const std::string& word, int maxAttempts = 10) : wordToGuess(word), maxAttempts(maxAttempts) {
        guessedLetters = std::string(word.size(), '_');
    }

    string randomword; 

    void drawHangman() const {
        //rita ut gubbe, använd incorrectGuesses.size() för att veta hur många fel som gjorts
    }

    void displayStatus() const {
        clearScreen();
        drawHangman();
        cout << "Ord: ";
        for (char c : guessedLetters) {
            cout << c << " ";
        }
        cout << endl << "Felgissningar: ";
        for (char ch : incorrectGuesses) {
            cout << ch << " ";
        }
        cout << endl << "Antal f\u00F6rs\u00F6k kvar: " << maxAttempts - incorrectGuesses.size() << endl;
    }

    bool guess(char letter) {
        letter = tolower(letter);
        bool correct = false;
        if(isLetterGuessed(letter))
        {
            cout <<"Du har redan gissat på bokstaven " << letter <<" försök igen!" << endl;
            #ifdef _WIN32
            Sleep(2000);
            #else
            sleep(2000);
            #endif
            return false;
        }
        for (size_t i = 0; i < wordToGuess.size(); ++i) {
            if (wordToGuess[i] == letter) {
                guessedLetters[i] = letter;
                correct = true;
            }
        }

        // kolla här om vår random letter för twist träffas, pusha in samma bokstav 2 ggr?
        if (!correct) {
            incorrectGuesses.push_back(letter);
        }

        return correct;
    }

    bool endOfAttempts() const
    {
        return incorrectGuesses.size() >= maxAttempts || guessedLetters == wordToGuess;
    }

    bool win() const
    {
        return guessedLetters == wordToGuess;
    }

    bool isLetterGuessed(char letter) {
        // Kontrollera om bokstaven finns i guessedLetters eller incorrectGuesses
        if (find(guessedLetters.begin(), guessedLetters.end(), letter) != guessedLetters.end()) {
            return true;
        }
        if (find(incorrectGuesses.begin(), incorrectGuesses.end(), letter) != incorrectGuesses.end()) {
            return true;
        }
        return false;
    }
};


vector<string> loadWordsFromFile(const string& filename) {
    vector<string> words;
    ifstream file(filename);
    if (!file) {
        cerr << "Could not open the file " << filename << "\n";
        return words;
    }

    string word;
    while (getline(file, word)) {
        if (!word.empty()) {
            words.push_back(word);
        }
    }
    return words;
}



