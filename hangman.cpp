void gamePlay(vector<string>& words, vector<string>& letters) {
  
    string randomWord = Randomizer(words);
    string randomLetter = Randomizer(letters);

    Game game(randomWord);

    cout << "Random word: " << randomWord << endl; // Debug
    cout << "Random letter: " << randomLetter << endl; // Debug
    int attempts = 0;
    while(!game.endOfAttempts()) {
        game.displayStatus();

        char guess;
        cout << "Gissa en bokstav: ";
        cin >> guess;
        

        if(!game.guess(guess)) {
            cout << "Fel gissning!" << endl;
            sleepForSeconds(2);
        } else {
            cout << "R\u00E4tt gissning!" << endl;
            sleepForSeconds(2);
        }
        attempts++;

        if(game.win()) {
            cout << "Grattis! Du vann! " << "Du gissade r\u00E4tt ord: " << randomWord << endl;
            cout << "Antal gissningar: " << attempts << endl;
            cin.ignore();
            cin.get();
            break;
        }
    }

    if(!game.win()) {
        cout << "Spelet \u00E4r \u00F6ver! Ordet var: " << randomWord << endl;
        cout << "Tryck på valfri knapp f\u00F6r att forts\u00E4tta" << endl;
        cin.ignore();
        cin.get();
    }
}

string Randomizer( vector<string> list) {
    random_device rd;
    mt19937 gen(rd());
    uniform_int_distribution<> dis(0, list.size() - 1);
    int index = dis(gen);
    return list.at(index);
}


